  <div>
    <h2 class="text-2xl pb-2">Of Harmonics and Spectra</h2>
    <p>
      Coincident Spectra is an additive synthesizer that generates
      sounds from 16 sine wave 'partials' that can be switched between
      Harmonics (integer multiples of the fundamental frequency) and
      Spectra, which remaps the frequency ratios of the harmonic series
      such that the partial tones are coincident with the selected equal
      temperament. Coincident spectra reduce sensory dissonance and
      beating between the timbre and tuning rendering chords more in
      tune.
    </p>
  </div>
  <div class="grid grid-flow-row auto-rows-max gap-2">
    <h2 class="text-2xl">Listen and Compare</h2>
