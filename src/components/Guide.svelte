      <li>Select Start Audio to activate the instrument</li>
      <li>Select Keyboard or a MIDI controller</li>
      <li>Play notes and chords in Harmonics mode</li>
      <li>Switch to Spectra mode. Listen and compare.</li>
    </ul>
